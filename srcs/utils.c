#include "fillit.h"

int		count_tetri(char *map)
{
	int i;
	int j;
	int ct;

	i = 0;
	ct = 21;
	j = 0;
	while(map[i])
	{
		if (i == ct)
		{
			j++;
			ct += 21;
		}
		i++;
	}
	j++;
	return (j);
}